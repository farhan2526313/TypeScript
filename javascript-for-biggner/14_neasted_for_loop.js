

// how to create star pattern

for(var a= 0;a <= 3 ; a++){
    
    for(var c=1 ; c<=a; c++){
        document.write("* ")
        }
    document.write("<br>")
    
}
    
for(var b= 0;b <= 3 ; b++){
    
    for(var d=3 ; d>=b; d--){
        document.write("* ")
        }
    document.write("<br>")
    
}
    
